package FourthProgram;
/**
 * This class stores the value of cars that come for service in service station 
 * @author Riddhi
 *
 */
public class Car {
	String carId;
	String carType;

	/**
	 * @param Car Id number
	 * @param Type of car
	 */
	public Car(String CarNum, String CarTyp)
	{
		carId = CarNum;
		carType = CarTyp;
	}
}
